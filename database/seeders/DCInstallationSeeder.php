    private function formatPinCode($pinCode)
    {
        if (!$pinCode) return null;
        return str_replace(' ', '', (string) $pinCode);
    }

    /**
     * Disable foreign key checks in a database-agnostic way
     */
    private function disableForeignKeyChecks()
    {
        $driver = DB::getDriverName();

        switch ($driver) {
            case 'mysql':
                DB::statement('SET FOREIGN_KEY_CHECKS=0;');
                break;
            case 'sqlite':
                DB::statement('PRAGMA foreign_keys=OFF;');
                break;
            case 'pgsql':
                // PostgreSQL doesn't have a global way to disable foreign keys
                // We'll handle this differently if needed
                break;
        }
    }

    /**
     * Enable foreign key checks in a database-agnostic way
     */
    private function enableForeignKeyChecks()
    {
        $driver = DB::getDriverName();

        switch ($driver) {
            case 'mysql':
                DB::statement('SET FOREIGN_KEY_CHECKS=1;');
                break;
            case 'sqlite':
                DB::statement('PRAGMA foreign_keys=ON;');
                break;
            case 'pgsql':
                // PostgreSQL doesn't have a global way to disable foreign keys
                break;
        }
    }
