    /**
     * Handle NOT NULL constraints by providing default values
     */
    private function handleNotNullConstraints($installation)
    {
        // Fields that might have NOT NULL constraints but we're setting to null
        $defaultValues = [
            'original_pod_received' => false,  // Default to false instead of null
            'installation_status' => 'Pending', // Default status for null values
            'delivery_status' => $installation['delivery_status'] ?? 'Pending',
            'soft_copy_dc' => $installation['soft_copy_dc'] ?? false,
            'soft_copy_ir' => $installation['soft_copy_ir'] ?? false,
            'original_dc_received' => $installation['original_dc_received'] ?? false,
            'ir_original_copy_received' => $installation['ir_original_copy_received'] ?? false,
            'back_side_photo_taken' => $installation['back_side_photo_taken'] ?? false,
            'os_installation_photo_taken' => $installation['os_installation_photo_taken'] ?? false,
            'belarc_report_generated' => $installation['belarc_report_generated'] ?? false,
            'is_active' => $installation['is_active'] ?? true,
            'priority' => $installation['priority'] ?? 'Medium',
            'created_by' => $installation['created_by'] ?? 1,
            'updated_by' => $installation['updated_by'] ?? 1,
        ];

        // Override null values with defaults
        foreach ($defaultValues as $field => $defaultValue) {
            if ($installation[$field] === null) {
                $installation[$field] = $defaultValue;
            }
        }

        return $installation;
    }

    /**
     * Helper method to convert boolean strings to boolean values
     */
    private function convertBoolean($value)
    {
        if ($value === 'Yes') return true;
        if ($value === 'No') return false;
        return null;
    }

    /**
